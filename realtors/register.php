<?php
include('includes/db.php');
include('includes/functions.php');

if ($_SERVER["REQUEST_METHOD"] == "POST") {
    $username = $conn->real_escape_string($_POST['username']);
    $email = $conn->real_escape_string($_POST['email']);
    $password = password_hash($conn->real_escape_string($_POST['password']), PASSWORD_BCRYPT);

    $query = "INSERT INTO users (username, email, password) VALUES ('$username', '$email', '$password')";
    if ($conn->query($query) === TRUE) {
        // Send confirmation email
        $subject = "Welcome to Elcent Realtors";
        $message = "Thank you for registering with Elcent Realtors. Your account has been created successfully.";
        mail($email, $subject, $message);
        echo "Registration successful. A confirmation email has been sent to $email.";
    } else {
        echo "Error: " . $query . "<br>" . $conn->error;
    }
}
?>

